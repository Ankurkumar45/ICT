	<h3>Prize money</h3>
	<p>The 2019 season of the IPL offered a total prize money of ₹50 crore (equivalent to ₹53 crore or US$6.6 million in 2020), with the winning team netting ₹20 crore (equivalent to ₹21 crore or US$2.6 million in 2020). The first and second runners up received ₹12.5 crore (US$1.6 million) and ₹8.75 crore (US$1.1 million), respectively, with the fourth placed team also winning ₹8.75 crore (US$1.1 million). The other teams are not awarded any prize money. The IPL rules mandate that half of the prize money must be distributed among the players.</p>
	<h2>Rules</h2>
	<p>The IPL has some unique rules which the T20I format does not.</p>
	<ul class="ipl_rules">
		<li>Strategic Timeout - IPL games incorporate television timeouts. A penalty may be imposed if the umpires find teams misusing this privilege. Each team is given a 2:30 "strategic timeout" during each innings; one must be taken by the bowling team between the ends of the 6th and 9th overs, and one must be taken by the batting team between the ends of the 13th and 16th overs. To indicate the strategic timeout, the on-field umpire will raise his hand in the air and tap his wrist. This rule is borrowed from the NBA.</li><br>
		<li>Since the 2018 season, the Decision Review System (DRS) is being used in all IPL matches, allowing each team two chances per innings to review an on-field umpire's decision. From the 2023 season, players can review wides and no-balls, which is not the case in other men's cricket tournaments.</li><br>
		<li>If the bowling team doesn't complete its overs in allocated time, then they must place only 4 fielders outside of the 30-yard circle for the remainder of the innings.</li><br>
		<li>Impact Player (Substitution) - Teams can use a substitute, which is termed as an "Impact player". For this substitution, the teams must handover a list of five players as probable substitutes to the match referee. The substitution can only be made before the start of an inning, when a wickets falls, when a batter retires, or at the end of an over. Teams can declare their playing XI and list of Impact Players after the toss, but all five Impact Players must be Indian. This rule is borrowed from football and the NBA. Both teams can introduce a substitute once per match. If a batsman gets out and the bowling team decides to make a substitution in the middle of the over, the substitute bowler cannot bowl the remaining balls of that over. The replaced player cannot take part in the match, even as a substitute fielder. Teams can introduce a foreign player as a substitute if they have included less than 4 foreign players in their playing XI.</li><br>
		<li>There will be penalty of five runs if a fielder or wicketkeeper makes an unfair movement while the bowler is bowling and before ball reaches the batsman</li><br>
		<li>After the fall of a wicket, the new batsman always will take the strike.</li><br>
		<li>Teams can incorporate maximum 4 foreign players in the playing XI of 11.</li><br>
		<li>Teams must include 25 players in their squad, and they can include only 8 foreign players.</li><br>
	</ul>

