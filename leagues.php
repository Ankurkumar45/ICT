	<h2 align="center" style="font-size: 30px">Awards</h2>
	<h4>Orange Cap</h4>
	<p>The Orange Cap is awarded to the top run-scorer in the IPL during a season. It is an ongoing competition with the leader wearing the cap throughout the tournament until the final game, with the eventual winner keeping the cap for the season. Latest winner – Jos Buttler – 863 Runs (2022).</p>
	<h4>Purple Cap</h4>
	<p>The Purple Cap is awarded to the top wicket-taker in the IPL during a season. It is an ongoing competition with the leader wearing the cap throughout the tournament until the final game, with the eventual winner keeping the cap for the season. Latest winner – Yuzvendra Chahal – 27 wickets (2022).</p>
	<h4>Most Valuable Player(MVP)</h4>
	<p>The award was called the "man of the tournament" until the 2012 season. The IPL introduced the Most Valuable Player rating system in 2013, the leader of which would be named the "Most Valuable Player" at the end of the season. Latest winner – Jos Buttler (2022).</p>
	<h4>Fairplay Award</h4>
	<p>The Fair Play Award is given after each season to the team with the best record of fair play. The winner is decided on the basis of the points the umpires give to the teams. After each match, the two on-field umpires, and the third umpire, scores the performance of both teams. Latest winners –Rajasthan Royals.</p>
	<h4>Emerging Player Award</h4>
	<p>The award was presented for the "best under-19 player" in 2008 and "best under-23 player" in 2009 and 2010, being called "Under-23 Success of the Tournament". In 2011 and 2012, the award was known as "Rising Star of the Year", while, in 2013, it was called "Best Young Player of the Season". Since 2014, the award has been called the Emerging Player of the Year. In 2016, Mustafizur Rahman of Bangladesh was the first and only foreign player to date to win the Emerging Player of the Year award. Latest winner – Umran Malik (2022).</p>
	<h4>Maximum Sixes Award</h4>
	<p>The award for the most sixes in a season, currently known as Unacademy Let's Crack It Sixes Award for sponsorship reasons, is presented to the batsman who hits the most sixes in a season of the IPL. English batsman Jos Buttler of the Rajasthan Royals won this award in 2022 with 45 sixes in 17 innings.</p>

	<h1 style="border-bottom: 2px solid yellow;">Women's Premier League (cricket)</h1>
	<h3>Organisation</h3>
	<p>The league's structure is based on the structure of the IPL.

		Initially there are five teams, with sides playing against each other in a double round robin format, and the three teams finishing with the most points entering the playoff stages of the competition. The Board plans to increase the number of matches and franchises in future seasons if the league is a success.

		The first season of the league took place from 4 March to 26 March 2023, and featured 22 matches, all held at Brabourne Stadium and DY Patil Stadium in Mumbai. Tickets were made available free to women during the first season.

		The league's mascot, Shakti, is a tigress wearing a sky blue cricket uniform.</p>
	<h3>Franchises</h3>
	<p>Investors brought the initial franchise rights in January 2023 through a closed bidding process, raising a total of ₹4,669 crore (US$580 million).</p>
	<p>A number of companies responded to the sale of franchise rights which were sold for five years, from 2023 to 2027. Adani Group won the rights to the Ahmedabad franchise for ₹1,289 crore (US$160 million), Indiawin Sports Pvt Ltd, part of Reliance Industries, won the Mumbai franchise for ₹912.99 crore (US$110 million), GMR–JSW Cricket Pvt Ltd won the Delhi franchise for ₹810 crore (US$100 million), Capri Global holdings won the Lucknow franchise for ₹757 crore (US$95 million), and Royal Challengers Sports Pvt Ltd, a subsidiary of alcohol manufacturing company Diageo, won the Bangalore franchise for ₹901 crore (US$110 million).</p>

