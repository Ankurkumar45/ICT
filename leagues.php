	<h1 style="border-bottom: 2px solid yellow;">Indian Premier League</h1>
	<p>The Indian Premier League (IPL) (also known as the TATA IPL for sponsorship reasons) is a men's Twenty20 (T20) cricket league held annually in India. It is contested by ten teams based in seven cities and three states of India. The league was founded by the Board of Control for Cricket in India (BCCI) in 2007. Brijesh Patel is the incumbent chairman of the IPL. Competition is usually held annually in summer (between March and May) and has an exclusive window in the ICC Future Tours Programme, meaning that international cricket does not take place during that the IPL season.</p>
	<p>The IPL is the most popular cricket league in the world, and in 2014 it was ranked sixth by average attendance among all sports leagues.[needs update] In 2010, the IPL became the first sporting event in the world to be broadcast live on YouTube. The brand value of the IPL in 2022 was ₹90,038 crore (US$11 billion). According to BCCI, the 2015 IPL season contributed ₹1,150 crore (US$140 million) to the GDP of the Indian economy. In December 2022, the league became a decacorn valued at $10.9 billion registering a 75% growth in dollar terms since 2020 when it was valued at $6.2 billion, according to a report by consulting firm D & P Advisory.</p>
	<p>There have been fifteen seasons of the tournament. The current title holders are the Gujarat Titans, winning the 2022 title in what was their first season.</p>
